use aoc20;
use std::str::FromStr;
use thiserror::Error;

fn main() {
    let mut input = aoc20::ProgramInput::new(PUZZLE, INPUT);
    println!("Day 3: {}", PUZZLE);

    let map: Map = input.to_str().parse().unwrap();

    println!(
        ":: The number of trees encountered is: {}",
        map.traverse_trees(3, 1)
    );

    let angles: Vec<(usize, usize)> = vec![(1, 1), (3, 1), (5, 1), (7, 1), (1, 2)];
    println!(
        ":: The product of all angle traversals is: {}",
        angles
            .iter()
            .map(|(cols, rows)| map.traverse_trees(*cols, *rows))
            .product::<usize>()
    );
}

#[derive(Debug)]
pub struct Map {
    data: Vec<Vec<bool>>,
}

impl Map {
    pub fn columns(&self) -> usize {
        self.data[0].len()
    }

    pub fn rows(&self) -> usize {
        self.data.len()
    }

    pub fn is_valid(&self) -> bool {
        if self.data.is_empty() {
            return false;
        }
        let cols = self.columns();
        for row in &self.data {
            if row.len() != cols {
                return false;
            }
        }
        true
    }

    pub fn traverse_trees(&self, cols: usize, rows: usize) -> usize {
        let ncols = self.columns();
        let nrows = self.rows();

        let mut trees = 0;
        let mut col = 0;
        let mut row = 0;
        while row < nrows {
            if self.data[row][col] {
                trees += 1;
            }
            col = (col + cols) % ncols;
            row += rows;
        }
        trees
    }
}

#[derive(Error, Debug)]
pub enum MapError {
    #[error("invalid input")]
    InvalidInput { input: String },
}

impl FromStr for Map {
    type Err = MapError;

    fn from_str(s: &str) -> Result<Self, Self::Err> {
        if s.is_empty() {
            return Err(Self::Err::InvalidInput {
                input: s.to_string(),
            });
        }

        Ok(Self {
            data: s
                .trim()
                .lines()
                .map(|l| {
                    l.trim()
                        .chars()
                        .map(|x| match x {
                            '.' => false,
                            _ => true,
                        })
                        .collect()
                })
                .collect(),
        })
    }
}

